#!/usr/bin/env node
// Generate lib/themes/registry.ts by globbing lib/themes/*.ts. Brand agents only
// ever create lib/themes/<slug>.ts; the orchestrator runs this once per batch so
// no two agents touch a shared file. Deterministic (sorted), so the output is
// stable across runs. See docs/standard-template-architecture.md.
import { readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const themesDir = join(here, "..", "lib", "themes");
const RESERVED = new Set(["types", "neutral", "registry"]);

const slugs = readdirSync(themesDir)
  .filter((f) => f.endsWith(".ts"))
  .map((f) => f.slice(0, -3))
  .filter((name) => !RESERVED.has(name))
  .sort();

// Identifiers safe for import bindings (slugs contain hyphens/dots).
const ident = (slug) => "t_" + slug.replace(/[^a-zA-Z0-9]/g, "_");

const lines = [];
lines.push(
  "// GENERATED by scripts/gen-theme-registry.mjs. Do not edit by hand.",
  '// Regenerated once per batch by the orchestrator (Lane 2 fill-out).',
  'import type { BrandTheme } from "./types";',
  'import neutral from "./neutral";',
);
for (const slug of slugs) {
  lines.push(`import ${ident(slug)} from "./${slug}";`);
}
lines.push("");
lines.push("export const themes: Record<string, BrandTheme> = {");
for (const slug of slugs) {
  lines.push(`  ${JSON.stringify(slug)}: ${ident(slug)},`);
}
lines.push("};");
lines.push("");
lines.push("export const builtThemeSlugs: readonly string[] = " + JSON.stringify(slugs) + ";");
lines.push("");
lines.push("export function themeFor(slug: string): BrandTheme {");
lines.push("  return themes[slug] ?? neutral;");
lines.push("}");
lines.push("");
lines.push("export function hasTheme(slug: string): boolean {");
lines.push("  return slug in themes;");
lines.push("}");
lines.push("");

writeFileSync(join(themesDir, "registry.ts"), lines.join("\n"));
console.log(`gen-theme-registry: ${slugs.length} theme(s) -> lib/themes/registry.ts`);
if (slugs.length) console.log("  " + slugs.join(", "));
